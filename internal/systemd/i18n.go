package systemd

import "strings"

const (
	LocaleEnglish  = "en"
	LocaleJapanese = "ja"
)

func NormalizeLocale(locale string) string {
	locale = strings.ToLower(strings.TrimSpace(locale))
	if strings.HasPrefix(locale, "ja") {
		return LocaleJapanese
	}
	return LocaleEnglish
}

func (c *Catalog) SectionDocFor(section, locale string) string {
	if NormalizeLocale(locale) != LocaleJapanese {
		return c.SectionDoc(section)
	}
	if doc, ok := japaneseSectionDocs[section]; ok {
		return doc
	}
	return "systemd の [" + section + "] セクションです。"
}

func (c *Catalog) SectionDocumentationFor(section, locale string) string {
	doc := c.SectionDocFor(section, locale)
	syntax := "[" + section + "]"
	example := sectionExample(section)
	return markdownDoc(doc, "", syntax, example)
}

func DirectiveDocFor(section string, directive Directive, locale string) string {
	if NormalizeLocale(locale) != LocaleJapanese {
		if directive.Doc != "" {
			return directive.Doc
		}
		if directive.ValueKind != "" {
			return "systemd directive for the [" + section + "] section. Value kind: " + directive.ValueKind + "."
		}
		return "systemd directive for the [" + section + "] section."
	}
	if doc, ok := japaneseDirectiveDocs[directive.Name]; ok {
		return doc
	}
	return "systemd の [" + section + "] セクションで使用する " + directive.Name + " ディレクティブです。"
}

func DirectiveDocumentationFor(section string, directive Directive, locale string) string {
	doc := DirectiveDocFor(section, directive, locale)
	syntax := directiveSyntax(directive)
	example := directiveExample(directive)
	return markdownDoc(doc, directive.ManPage, syntax, example)
}

func markdownDoc(description, manPage, syntax, example string) string {
	parts := []string{description}
	if manPage != "" {
		parts = append(parts, "`"+manPage+"`")
	}
	lines := make([]string, 0, 2)
	firstExampleLine, _, _ := strings.Cut(example, "\n")
	if syntax != "" && strings.TrimSuffix(firstExampleLine, "\r") != syntax {
		lines = append(lines, syntax)
	}
	if example != "" {
		lines = append(lines, example)
	}
	parts = append(parts, "```ini\n"+strings.Join(lines, "\n")+"\n```")
	return strings.Join(parts, "\n\n")
}

func sectionExample(section string) string {
	if example, ok := sectionExamples[section]; ok {
		return example
	}
	return "[" + section + "]"
}

func directiveSyntax(directive Directive) string {
	if directive.Syntax != "" {
		return directive.Syntax
	}
	value := "<value>"
	if len(directive.Values) > 0 {
		value = strings.Join(directive.Values, "|")
	} else if directive.ValueKind != "" {
		value = "<" + directive.ValueKind + ">"
	}
	return directive.Name + "=" + value
}

func directiveExample(directive Directive) string {
	if directive.Example != "" {
		return directive.Example
	}
	if example, ok := directiveExamples[directive.Name]; ok {
		return example
	}
	if len(directive.Values) > 0 {
		return directive.Name + "=" + directive.Values[0]
	}
	return directive.Name + "=<value>"
}

